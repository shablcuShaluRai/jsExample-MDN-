
// charAt returns the  specified character  from string.

var str ="brave technology";
console.log(str.charAt()); // default it returns first element of the string
console.log(str.charAt(3)); // it returns the  fourth element of string
console.log(str.charAt(str.length -1)); // it return the last element
console.log(str.charAt(str.length)); // it returns the  space
